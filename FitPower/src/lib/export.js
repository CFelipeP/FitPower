export function exportToCSV(data, filename = 'export.csv') {
    if (!data.length) return
    const headers = Object.keys(data[0])
    const csvContent = [
        headers.join(','),
        ...data.map(row => headers.map(h => {
            const val = row[h]
            if (val === null || val === undefined) return ''
            const str = String(val)
            if (str.includes(',') || str.includes('"') || str.includes('\n')) {
                return '"' + str.replace(/"/g, '""') + '"'
            }
            return str
        }).join(','))
    ].join('\n')

    const blob = new Blob([csvContent], { type: 'text/csv;charset=utf-8;' })
    const link = document.createElement('a')
    link.href = URL.createObjectURL(blob)
    link.download = filename
    link.click()
    URL.revokeObjectURL(link.href)
}

export function exportToPDF(title, data, columns) {
    const style = `
    <style>
        body { font-family: 'Inter', system-ui, sans-serif; padding: 40px; color: #111; }
        h1 { font-size: 24px; margin-bottom: 8px; color: #FFD600; }
        .subtitle { color: #666; margin-bottom: 24px; }
        table { width: 100%; border-collapse: collapse; }
        th { text-align: left; padding: 10px 12px; background: #f5f5f5; font-size: 12px; text-transform: uppercase; letter-spacing: 0.5px; color: #666; border-bottom: 2px solid #eee; }
        td { padding: 10px 12px; border-bottom: 1px solid #eee; font-size: 14px; }
        tr:last-child td { border-bottom: none; }
        .footer { margin-top: 32px; font-size: 12px; color: #999; text-align: center; }
    </style>`

    const html = `
    <!DOCTYPE html>
    <html>
    <head><meta charset="utf-8"><title>${escapeHtml(title)}</title>${style}</head>
    <body>
        <h1>⚡ ${escapeHtml(title)}</h1>
        <p class="subtitle">Generated on ${new Date().toLocaleDateString()}</p>
        <table>
            <thead>
                <tr>${columns.map(c => `<th>${escapeHtml(c.label)}</th>`).join('')}</tr>
            </thead>
            <tbody>
                ${data.map(row => `<tr>${columns.map(c => `<td>${escapeHtml(row[c.key]) ?? '—'}</td>`).join('')}</tr>`).join('')}
            </tbody>
        </table>
        <div class="footer">FitPower — Train Without Limits</div>
        <script>window.print()</script>
    </body>
    </html>`

    const win = window.open('', '_blank')
    win.document.write(html)
    win.document.close()
}

export function exportProgressData(metrics) {
    if (!metrics?.length) return
    const columns = [
        { key: 'date', label: 'Date' },
        { key: 'weight', label: 'Weight (kg)' },
        { key: 'bodyFat', label: 'Body Fat (%)' },
        { key: 'muscle', label: 'Muscle Mass (kg)' },
        { key: 'bmi', label: 'BMI' },
    ]
    const data = metrics.map(m => ({
        date: m.created_at ? new Date(m.created_at).toLocaleDateString() : '—',
        weight: m.weight ?? '—',
        bodyFat: m.bodyFat ?? m.body_fat ?? '—',
        muscle: m.muscle ?? '—',
        bmi: m.bmi ?? '—',
    }))
    exportToPDF('Progress Report', data, columns)
}

export function exportWorkoutData(sessions) {
    if (!sessions?.length) return
    const columns = [
        { key: 'title', label: 'Workout' },
        { key: 'date', label: 'Date' },
        { key: 'type', label: 'Type' },
        { key: 'exercises', label: 'Exercises' },
        { key: 'status', label: 'Status' },
    ]
    const data = sessions.map(s => ({
        title: s.title || s.name || 'Workout',
        date: s.date ? new Date(s.date).toLocaleDateString() : '—',
        type: s.type || '—',
        exercises: Array.isArray(s.exercises) ? s.exercises.length : '—',
        status: s.status || '—',
    }))
    exportToPDF('Workout History', data, columns)
}
